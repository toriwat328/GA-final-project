

$('#East_Harlem').on('click', () => {

    if($('#East_Harlem').hasClass("black")){

        $('#East_Harlem').removeClass("black");
        $('#East_Harlem').addClass("red");


    } else if($('#East_Harlem').hasClass("red")){
        $('#East_Harlem').removeClass("red");
        $('#East_Harlem').addClass("black");


    }

})

